// JWT ellen≈ërz√©s middleware
function verifyToken(req, res, next) {
  const authHeader = req.headers.authorization;
  if (!authHeader)
    return res.status(401).json({ message: "Nincs hiteles√≠t√©s, k√©rlek jelentkezz be." });

  const token = authHeader.split(" ")[1];
  try {
    const decoded = jwt.verify(token, process.env.JWT_SECRET || "titkoskulcs");
    req.user = decoded;
    next();
  } catch (err) {
    return res.status(403).json({ message: "√ârv√©nytelen token." });
  }
}

