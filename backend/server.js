// Ez a funkci√≥ t√∂rli azokat a tageket, amikhez nincs image_tags kapcsolat
async function cleanupUnusedTags() {
  try {
    const conn = await pool.getConnection();

    // üîç T√∂r√∂lj√ºk azokat a tageket, amelyekhez nincs kapcsol√≥d√≥ image_tags rekord
    const [result] = await conn.execute(`
      DELETE FROM tags
      WHERE id NOT IN (SELECT DISTINCT tag_id FROM image_tags)
    `);

    if (result.affectedRows > 0) {
      console.log(`üßπ ${result.affectedRows} haszn√°latlan tag t√∂r√∂lve az adatb√°zisb√≥l.`);
    } else {
      console.log("‚úÖ Nincsenek t√∂rlend≈ë tagek ‚Äì adatb√°zis tiszta.");
    }

    conn.release();
  } catch (err) {
    console.error("‚ùå Hiba az √°rva tagek tiszt√≠t√°sa k√∂zben:", err.message);
  }
}

